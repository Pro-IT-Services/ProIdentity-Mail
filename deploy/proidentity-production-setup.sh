ensure_placeholder_cert() {
  local host="$1"
  local live_dir="/etc/letsencrypt/live/${host}"
  if [[ -s "${live_dir}/fullchain.pem" && -s "${live_dir}/privkey.pem" ]]; then
    return
  fi
  install -d -m 0755 "${live_dir}"
  openssl req -x509 -newkey rsa:2048 -sha256 -days 7 -nodes \
    -subj "/CN=${host}" \
    -keyout "${live_dir}/privkey.pem" \
    -out "${live_dir}/fullchain.pem" >/dev/null 2>&1
  chmod 0600 "${live_dir}/privkey.pem"
}

