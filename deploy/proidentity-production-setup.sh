#!/usr/bin/env bash
set -euo pipefail

# ProIdentity Mail full server bootstrap.
#
# Expected artifact directory:
#   webadmin webmail groupware mailctl apply-mail-config
#
# Common overrides:
#   ARTIFACT_DIR=/tmp/proidentity-release
#   PUBLIC_IPV4=46.225.87.251
#   MAIL_HOSTNAME=mail.example.com
#   ADMIN_HOSTNAME=madmin.example.com
#   WEBMAIL_HOSTNAME=webmail.example.com
#   DAV_HOSTNAME=webmail.example.com
#   AUTOCONFIG_HOSTNAME=autoconfig.example.com
#   AUTODISCOVER_HOSTNAME=autodiscover.example.com
#   TLS_MODE=none|behind-proxy|letsencrypt-http|letsencrypt-dns-cloudflare|custom-cert
#   ADMIN_USERNAME=admin
#   ADMIN_PASSWORD=...
#   DB_NAME=proidentity_mail
#   DB_USER=proidentity_mail
#   DB_PASSWORD=...
#
# The same values can be provided as command-line options, for example:
#   ./proidentity-production-setup.sh \
#     --artifact-dir /tmp/proidentity-release \
#     --public-ipv4 46.225.87.251 \
#     --mail-hostname mail.example.com \
#     --admin-hostname madmin.example.com \
#     --webmail-hostname webmail.example.com \
#     --tls-mode letsencrypt-dns-cloudflare
#
# Passwords, DB credentials, auth-policy tokens, and backup encryption keys are
# autogenerated when not supplied by option, environment, or an existing env file.

ARTIFACT_DIR="${ARTIFACT_DIR:-/tmp/proidentity-release}"
ENV_DIR="/etc/proidentity-mail"
ENV_FILE="${ENV_FILE:-${ENV_DIR}/proidentity-mail.env}"
ADMIN_ENV_FILE="${ADMIN_ENV_FILE:-${ENV_DIR}/webadmin.env}"
WEBMAIL_ENV_FILE="${WEBMAIL_ENV_FILE:-${ENV_DIR}/webmail.env}"
GROUPWARE_ENV_FILE="${GROUPWARE_ENV_FILE:-${ENV_DIR}/groupware.env}"
BIN_DIR="/opt/proidentity-mail/bin"
SUMMARY_FILE="${SUMMARY_FILE:-/root/proidentity-install-summary.txt}"
BACKUP_ROOT="${BACKUP_ROOT:-/root/proidentity-backups}"
INSTALL_TS="$(date +%Y%m%d-%H%M%S)"

usage() {
  cat <<'USAGE'
Usage: proidentity-production-setup.sh [options]

Installs a complete ProIdentity Mail server:
  - MariaDB database and user
  - Postfix, Dovecot, Rspamd, ClamAV, Redis, Unbound, Nginx
  - webadmin, webmail, groupware/DAV, mailctl, config apply helper
  - systemd services/timers, backup job, TLS worker, config drift/apply queue
  - rendered mail/proxy configs and initial migrations

Options override environment variables and existing env-file values:
  --artifact-dir PATH              Directory containing webadmin, webmail, groupware, mailctl, apply-mail-config
  --mail-hostname HOST             Mail server hostname, usually mail.example.com
  --admin-hostname HOST            Admin UI hostname
  --webmail-hostname HOST          Webmail UI hostname
  --dav-hostname HOST              CalDAV/CardDAV hostname
  --autoconfig-hostname HOST       Thunderbird autoconfig hostname
  --autodiscover-hostname HOST     Outlook autodiscover hostname
  --public-hostname DOMAIN         Base public domain used for discovery defaults
  --public-ipv4 IP                 Public IPv4 used in generated DNS records
  --public-ipv6 IP                 Public IPv6 used in generated DNS records
  --tls-mode MODE                  none|behind-proxy|letsencrypt-http|letsencrypt-dns-cloudflare|custom-cert
  --proxy-mode MODE                internal-nginx
  --trust-proxy-headers BOOL       true when an upstream trusted proxy sets real IP headers
  --trusted-proxy-cidrs CIDRS      Comma-separated trusted proxy CIDRs
  --cloudflare-cert-domain DOMAIN  Cloudflare domain for DNS-01 certificate automation
  --cloudflare-credentials-file PATH
  --tls-cert-path PATH             Custom HTTPS/mail certificate path
  --tls-key-path PATH              Custom HTTPS/mail private key path
  --admin-username USER
  --admin-password PASSWORD        Autogenerated if omitted
  --db-name NAME                   Default: proidentity_mail
  --db-user USER                   Default: proidentity_mail
  --db-password PASSWORD           Autogenerated if omitted
  --backup-encryption-key SECRET   Autogenerated if omitted
  --auth-policy-token SECRET       Autogenerated if omitted
  --auth-policy-nonce SECRET       Autogenerated if omitted
  --env-file PATH
  --summary-file PATH
  --backup-root PATH
  -h, --help

Any omitted secret is generated with openssl and written to the root-only
summary file, default /root/proidentity-install-summary.txt.
USAGE
}

arg_value() {
  local current="$1"
  local next="${2:-}"
  if [[ "${current}" == *=* ]]; then
    printf '%s' "${current#*=}"
    return 0
  fi
  if [[ -z "${next}" || "${next}" == --* ]]; then
    echo "Missing value for ${current%%=*}" >&2
    exit 2
  fi
  printf '%s' "${next}"
}

