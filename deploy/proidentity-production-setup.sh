random_secret() {
  local bytes="${1:-32}"
  openssl rand -hex "${bytes}"
}

require_file() {
  local path="$1"
  if [[ ! -s "${path}" ]]; then
    echo "Required artifact missing: ${path}" >&2
    exit 2
  fi
}

validate_ident() {
  local name="$1"
  local value="$2"
  if [[ ! "${value}" =~ ^[A-Za-z0-9_]+$ ]]; then
    echo "${name} must contain only letters, numbers, and underscores" >&2
    exit 2
  fi
}

install_env_file() {
  local path="$1"
  local owner="$2"
  local group="$3"
  local mode="$4"
  install -m "${mode}" -o "${owner}" -g "${group}" /dev/null "${path}"
}

set_env_file() {
  local file="$1"
  local key="$2"
  local value="$3"
  local escaped
  escaped="$(printf '%s' "${value}" | sed 's/[\/&]/\\&/g')"
  if grep -q "^${key}=" "${file}" 2>/dev/null; then
    sed -i "s/^${key}=.*/${key}=${escaped}/" "${file}"
  else
    printf '%s=%s\n' "${key}" "${value}" >> "${file}"
  fi
}

set_env() {
  set_env_file "${ENV_FILE}" "$@"
}

sql_string() {
  local value="$1"
  value="$(printf '%s' "${value}" | sed "s/\\\\/\\\\\\\\/g; s/'/''/g")"
  printf "'%s'" "${value}"
}

detect_public_ipv4() {
  curl -fsS --max-time 5 https://api.ipify.org 2>/dev/null || true
}

discovery_base_from_mail_hostname() {
  local host="${1%.}"
  host="${host,,}"
  if [[ "${host}" == *.local || "${host}" != *.* ]]; then
    return
  fi
  if [[ "${host}" == mail.* ]]; then
    printf '%s' "${host#mail.}"
  fi
}

default_discovery_hostname() {
  local prefix="$1"
  local base="${PUBLIC_HOSTNAME}"
  if [[ -z "${base}" ]]; then
    base="$(discovery_base_from_mail_hostname "${MAIL_HOSTNAME}")"
  fi
  if [[ -n "${base}" ]]; then
    printf '%s.%s' "${prefix}" "${base}"
  fi
}

backup_existing() {
  local backup_dir="${BACKUP_ROOT}/${INSTALL_TS}-production-setup"
  install -d -m 0700 "${backup_dir}"
  for path in "${ENV_DIR}" /etc/systemd/system/proidentity-webadmin.service /etc/systemd/system/proidentity-webmail.service /etc/systemd/system/proidentity-groupware.service /etc/systemd/system/proidentity-backup.service /etc/systemd/system/proidentity-config-apply.service /etc/systemd/system/proidentity-config-apply.path /etc/nginx/conf.d/proidentity.conf /etc/postfix/main.cf /etc/dovecot/conf.d/99-proidentity.conf /etc/unbound/unbound.conf.d/proidentity-dnsbl.conf /etc/resolvconf/resolv.conf.d/head /etc/dhcp/dhclient.conf /etc/network/interfaces.d/50-cloud-init; do
    if [[ -e "${path}" ]]; then
      cp -a "${path}" "${backup_dir}/" 2>/dev/null || true
    fi
  done
  echo "${backup_dir}"
}

