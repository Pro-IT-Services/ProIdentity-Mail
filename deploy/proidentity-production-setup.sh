issue_certificate_if_requested() {
  case "${TLS_MODE}" in
    letsencrypt-http)
      "${BIN_DIR}/mailctl" sync-proxy --reload
      "${BIN_DIR}/proidentity-issue-cert" || echo "warning: certbot HTTP challenge failed; placeholder certificate remains" >&2
      ;;
    letsencrypt-dns-cloudflare)
      if [[ -z "${CLOUDFLARE_CREDENTIALS_FILE}" || ! -s "${CLOUDFLARE_CREDENTIALS_FILE}" ]]; then
        echo "warning: Cloudflare credentials missing; DNS certificate not issued yet" >&2
      else
        "${BIN_DIR}/mailctl" sync-proxy --reload
        "${BIN_DIR}/proidentity-issue-cert" || echo "warning: certbot DNS challenge failed; placeholder certificate remains" >&2
      fi
      ;;
  esac
}

