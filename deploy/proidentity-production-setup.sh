configure_dns_resolver() {
  echo "Configuring local recursive DNS resolver for DNSBL checks..."
  install -d -m 0755 /etc/unbound/unbound.conf.d
  cat > /etc/unbound/unbound.conf.d/proidentity-dnsbl.conf <<'EOF'
server:
  interface: 127.0.0.1
  interface: ::1
  access-control: 127.0.0.0/8 allow
  access-control: ::1 allow
  do-ip4: yes
  do-ip6: yes
  do-udp: yes
  do-tcp: yes
  hide-identity: yes
  hide-version: yes
  harden-glue: yes
  harden-dnssec-stripped: yes
  qname-minimisation: yes
  prefetch: yes
  rrset-roundrobin: yes
EOF

  install -d -m 0755 /etc/resolvconf/resolv.conf.d
  cat > /etc/resolvconf/resolv.conf.d/head <<'EOF'
# ProIdentity local resolver for DNSBL; public DNS as fallback only
nameserver 127.0.0.1
nameserver 1.1.1.1
nameserver 8.8.8.8
EOF

  if [[ -f /etc/dhcp/dhclient.conf ]]; then
    if grep -q "^supersede domain-name-servers" /etc/dhcp/dhclient.conf; then
      sed -i "s/^supersede domain-name-servers.*/supersede domain-name-servers 1.1.1.1, 8.8.8.8;/" /etc/dhcp/dhclient.conf
    else
      printf '\n# ProIdentity preferred public resolvers\nsupersede domain-name-servers 1.1.1.1, 8.8.8.8;\n' >> /etc/dhcp/dhclient.conf
    fi
  fi

  if [[ -f /etc/network/interfaces.d/50-cloud-init ]]; then
    sed -i 's/^\([[:space:]]*dns-nameservers\).*/\1 2606:4700:4700::1111 2001:4860:4860::8888/' /etc/network/interfaces.d/50-cloud-init || true
  fi

  unbound-checkconf
  systemctl enable --now unbound
  systemctl restart unbound
  if command -v resolvconf >/dev/null 2>&1; then
    printf 'nameserver 1.1.1.1\nnameserver 8.8.8.8\n' | resolvconf -a eth0.dhclient || true
    printf 'nameserver 2606:4700:4700::1111\nnameserver 2001:4860:4860::8888\n' | resolvconf -a eth0.inet6 || true
    resolvconf -u || true
  fi
}

ensure_users() {
  if ! getent group vmail >/dev/null; then
    groupadd --system --gid 5000 vmail 2>/dev/null || groupadd --system vmail
  fi
  if ! id vmail >/dev/null 2>&1; then
    useradd --system --uid 5000 --gid vmail --home /var/vmail --shell /usr/sbin/nologin vmail 2>/dev/null || \
      useradd --system --gid vmail --home /var/vmail --shell /usr/sbin/nologin vmail
  fi
  if ! id proidentity >/dev/null 2>&1; then
    useradd --system --home /opt/proidentity-mail --shell /usr/sbin/nologin proidentity
  fi
  gpasswd -d vmail proidentity >/dev/null 2>&1 || true
  usermod -a -G vmail proidentity || true
  usermod -a -G postfix proidentity || true
  usermod -a -G dovecot proidentity || true
}

